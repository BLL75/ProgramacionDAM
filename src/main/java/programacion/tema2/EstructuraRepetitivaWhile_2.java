public class EstructuraRepetitivaWhile_2 {
    
    public static void main(String[] args){
        
        int num1 = 1;
        int num2 = 7;
        
        while (num1 <= num2){
            System.out.println(num1 + " * " + num2 + " = " + (num1 * num2) + "\n"  );
            num1++;
        }
    }
    
}
